//
// Sprites live at public/armor/<path>_<tier>.png (64x96) and
// public/armor/weapon_<path>.png (48x64), generated by waves of PixelLab
// create_map_object calls. Unlock strings reuse the avatarParts `<path>_<pct>`
// format so isUnlocked() resolves them against the user's `completed` map.

