// Flatten LINES into a single id → set map. id is `<path>_<tier>`.
export const ARMOR_SETS = (() => {
  const out = {};
  for (const path of Object.keys(LINES)) {
    for (const tier of ARMOR_TIERS) {
      const meta = LINES[path][tier];
      const id = `${path}_${tier}`;
      out[id] = {
        id,
        path,
        tier,
        name: meta.name,
        epithet: meta.epithet,
        pct: TIER_PCT[tier],
        unlock: `${path}_${TIER_PCT[tier]}`,         // avatarParts `<path>_<pct>` format
        weapon: tier === 'gold' ? ARMOR_WEAPONS[path] : null,
      };
    }
  }
  return out;
})();

export const ARMOR_KEYS = Object.keys(ARMOR_SETS);

// Provinces in display order, each with its three tier set-ids — drives the
// AvatarCreator's grouped armor picker.
export const ARMOR_BY_PATH = Object.keys(LINES).map((path) => ({
  path,
  label: PATH_LABEL[path] || path,
  tiers: ARMOR_TIERS.map((tier) => `${path}_${tier}`),
}));

// Sprite URL for a set figure, respecting Vite's base path.
export const armorSrc = (id) =>
  `${import.meta.env.BASE_URL}armor/${id}.png`
    .replace(/\/{2,}/g, '/')
    .replace(':/', '://');

// Sprite URL for a province's legendary weapon icon.
export const weaponSrc = (path) =>
  `${import.meta.env.BASE_URL}armor/weapon_${path}.png`
    .replace(/\/{2,}/g, '/')
    .replace(':/', '://');

// Is this armor set unlocked for the given `completed` lesson map? Reuses the
// avatarParts resolver so the gating logic stays in one place.
export function isArmorUnlocked(setId, completed) {
  const set = ARMOR_SETS[setId];
  if (!set) return false;
  return isUnlocked(set.unlock, completed);
}

// Tooltip hint for a locked set, tier-aware ("Reach Gold (100%) in FAANG Prep").
export function armorUnlockHint(setId) {
  const set = ARMOR_SETS[setId];
  if (!set) return '';
  return `Reach ${TIER_LABEL[set.tier]} (${set.pct}%) in ${PATH_LABEL[set.path] || set.path}`;
}
